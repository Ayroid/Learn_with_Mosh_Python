# for loops in Python are a lot different from C & Java

for i in "Python":                          # A normal for loop for travesing a string
    print(i)

for j in ["Ayush","Gohan","Goku","Vegeta"]:  # for loop to print elements of a list
    print(j)

for k in range(1,10,2):                     # range function is used to give elements between the lower & upper limit with n as steps to be jumped
    print(k)

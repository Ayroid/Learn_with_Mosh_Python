# Lists are like arrays but much simpler in fucntionality

names = ["Ayush","Cheenu","Nitya","Gunnu","Piyush"]         # This is a list containing 5 stirngs(names)
print(names)  # Printing the whole list

# printing elements of a particular index
print(names[0])        # It will print the first element of the list
print(names[0:3])      # It will print all elements from lower limit to upper limit
print(names[1:])       # It will print all elements from 2nd index to the end
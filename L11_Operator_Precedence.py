# Operator Precedence

# parenthesis ()
# exponentiation **
# multiplication/division */    
# addition/subtraction +-

x = 10 + 3 * 2 ** 2
# firstly 2**2 gets solved & is multplied with 3 then added to 10. Final Answer = 22  
print(x)

x = (2 + 3) * 10 - 3
# firstly parenthesis gets solved, then multiplied by 10 & then 3 is subtracted from the answer. Final Answer = 47 
print(x) 